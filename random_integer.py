

#generating random integers and Floating point numbers


import random

#random integer between one and 10
random_integer=random.randint(1,10)
print("Random integer is: ",random_integer)


#random floating point number between 0 and 1
random_float=random.random()
print("Random float number is: ",random_float)


#random floating point number between 0 and 5
random_float=random.random()
print("Random float number is: ",(random_float)*5)